const catalogKeys = Object.freeze({
  zh_cn: Object.freeze(Object.keys(catalogs.zh_cn).sort()),
  en_us: Object.freeze(Object.keys(catalogs.en_us).sort())
})
const emptyCatalogKeys = Object.freeze([])

function hasOwn (object, key) {
  return Object.prototype.hasOwnProperty.call(object, key)
}

function isMeaningfulString (value) {
  return typeof value === 'string' && value.trim().length > 0
}

export function getShellPilotCatalogKeys (langId = 'zh_cn') {
  return hasOwn(catalogKeys, langId) ? catalogKeys[langId] : emptyCatalogKeys
}

export function getShellPilotTranslation (key, langId = 'zh_cn') {
  if (!hasOwn(catalogs, langId)) {
    return undefined
  }
  return hasOwn(catalogs[langId], key) ? catalogs[langId][key] : undefined
}

export function resolveShellPilotTranslation (key, langId = 'zh_cn', localeValue, englishValue, readableDefault) {
  const candidates = [
    getShellPilotTranslation(key, langId),
    localeValue,
    getShellPilotTranslation(key, 'en_us'),
    englishValue,
    readableDefault,
    key
  ]

  return candidates.find(isMeaningfulString) || ''
}

export function formatShellPilotTranslation (translate, key, replacements = {}) {
  const translated = typeof translate === 'function' ? translate(key) : ''
  const english = getShellPilotTranslation(key, 'en_us')
  const template = isMeaningfulString(translated) && translated !== key
    ? translated
    : english || key
  return Object.entries(replacements).reduce((result, [name, value]) => {
    return result.split(`{${name}}`).join(String(value))
  }, template)
}

export function createShellPilotRequiredRule (translate, fieldKey) {
  return () => {
    const translatedField = typeof translate === 'function' ? translate(fieldKey) : ''
    const field = isMeaningfulString(translatedField) && translatedField !== fieldKey
      ? translatedField
      : fieldKey
    return {
      required: true,
      message: formatShellPilotTranslation(translate, 'shellpilotFieldRequired', { field })
    }
  }
}

export function createShellPilotMaxRule (translate, count) {
  return () => ({
    max: count,
    message: formatShellPilotTranslation(translate, 'shellpilotMaxCharacters', { count })
  })
}

export function resolveShellPilotModalCopy (options = {}, translate) {
  const resolve = (key, fallback) => {
    const value = typeof translate === 'function' ? translate(key) : ''
    return isMeaningfulString(value) && value !== key ? value : fallback
  }
  return {
    okText: options.okText ?? resolve('ok', 'OK'),
    cancelText: options.cancelText ?? resolve('cancel', 'Cancel')
  }
}
